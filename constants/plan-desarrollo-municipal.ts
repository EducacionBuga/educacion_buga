// Función para obtener subprogramas de un programa específico
export function getSubprogramasByPrograma(nombrePrograma: string) {
  const programa = planesDesarrolloMunicipal.find(p => p.nombre === nombrePrograma)
  return programa?.subprogramas || []
}

// Función para obtener proyectos de un subprograma específico
export function getProyectosBySubprograma(nombrePrograma: string, nombreSubprograma: string) {
  const programa = planesDesarrolloMunicipal.find(p => p.nombre === nombrePrograma)
  const subprograma = programa?.subprogramas.find(s => s.nombre === nombreSubprograma)
  return subprograma?.proyectos || []
}

